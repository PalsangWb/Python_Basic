# Create a list using []
a = [10, 20 , 30, 40]

# Print the list using print() function
print(a)

# Access using index using a[0], a[1], a[2]
print(a[2])

# Change the value of list using
a[0] = 899
print(a)

# We can create a list with items of different types
c = [3.7, "Palsang", True, 10]
print(c)

# List slicing 
friends = ["Sunil", "Rabin", "Nischay", "Yubin", "Prashnat", "SHiva", 1]
print(friends[0:6])
print(friends[-3:])